								<img src="images/project-images/20151105_203111.jpg" alt="" class="responsive" width="370"/>
								<p class="small mb mt-small">My logical process to determine the serivce price. We wanted an average price-per-bag of $25 to compete with similar services. I researched the downtown-to-airport distance for a handful of major cities along with the average weight per checked suitcase. I fit the unit price such that an average ride to the airport for an average suitcase, when combined with a flat service rate, would equal our $25 target.</p>
