							<div class="col-md-8 col-md-offset-2">
								<p class="lead">
								Expedia has cornered many angles of the travel market, but while they have added many features in recent years to accommodate the highly organized vacation planner, one user segment missing from their domain was the last-minute wanderlust traveler. Who are these people who fill your instagram feed with travel dreams and seem to hop aboard a plane on a whim? Expedia asked us to investigate this user segment as the sponsor for our HCDE capstone project. <a href="process_book.pdf">See the full process book here.</a>
							</p>
								
                                <h3>The problem:</h3> <br>
                                    <p>
                                    When booking a trip within a week of departure, the last-minute traveler doesnâ€™t feel that there is a booking experience that offers the personalized, trustworthy, and flexible process they desire.

									</p>
                                    <div class="post-pic col-md-12 col-md-offset-0">
								<img src="images/project-images/Design_Process.png" alt="" class="responsive" />
								<p class="small mb mt-small">A visual representation of our design process.</p>
							</div>
                                    
									<p>
									We scoured 19 different popular travel booking websites like Hotwire, Priceline, and Google Flights. We surveyed 69 frequent flyers and then followed up with a more in-depth survey that reached 113 respondents. Additionally, we interviewed 8 of those respondents to drill into the facts behind their survey answers. These led us to the following research conclusions: 
                                        <ol>
                                        <li>
                                            Travelers can be overwhelmed by the wide selection of travel choices available.
</li>
                                <li>
                                            Travelers believe that travel websites are manipulative in how they present booking information to users.
</li>
                                <li>
                                            Travelers want a travel booking experience that does not feel like a huge commitment of time and money.
</li></ol>
								</p>
                            
                            <p>We translated these research conclusions into three overarching design goals: Trust, Curation, and Flexibility.</p>
                            
                            
                            <div class="post-pic col-md-12 col-md-offset-0">
								<img src="images/project-images/Design_Goals.png" alt="" class="responsive" />
								<p class="small mb mt-small">Our three most important goals to target.</p>
							</div>

							</div>
				


							<div class="col-md-8 col-md-offset-2 mb-medium">
									
								<h3>Trust us; we did the research</h3>
								<p>
									Taking those three design goals and running with them, we embarked on our prototyping process. This involved several whiteboarding sessions to deliver an Information Architecture flowchart and subsequent paper prototype screens to get quick test results from potential users. Some early things this helped us identify was our wording of certain tasks, like Favorites, Search vs. Browse, and the Sell-back feature that was meant to instill trust in our users.  
								</p>
							</div>
							<div class="post-pic col-md-4 col-md-offset-4">
								<img src="images/project-images/Capstone_IA.png" alt="" class="responsive" width="570"/>
								<p class="small mb mt-small">A basic diagram of the IA we settled on.</p>
							</div>
							<div class="col-md-8 col-md-offset-2">	
								<h3>Quick! Test it on a screen!</h3>
								<p>
								    After cleaning up our content strategy, we shifted our focus to the flow. As we wanted to design a travel shopping experience centered around quick gestures and swiping, building out an interactive experience was paramount. My teammate Val built a series of linked wireframes using Sketch to test users' ability to interact with the product. 
								</p>
							</div>

							<div class="col-md-8 col-md-offset-2">	
								<h3>Preparing for a smooth landing</h3>
								<p>
									The final phase of the project involved incorporating all of our user feedback from the wireframes into high fidelity screens. 
                                    
                                    What we changed: <br>
                                    <ul>
                                    <li><b>Swiping: </b>One of our key hypotheses in the deisgn process was to incorporate the swiping interactions of dating apps like Tinder to help the decision-making process. After all, finding a good vacation should feel like finding a good partner. Our user tests disagreed, possibly due to the fact that our prototypes couldn't mimic those gestures. Instead, our users indicated that they preferred to scroll through choices and see multiple options at a time for comparison sake.
                                        <img src="images/project-images/OfferPage.png" alt="" class="responsive" width="570"/></li>
                                    <li><b>Barriers to entry: </b>We deliberated for a while on whether or not we should include a sign-up button on the welcome screen. This is a platform that collects data,  personalizes interests, and includes financial transactions. Creating an account early seemed like a good idea. Our early tests hinted that users were unconvinced in the value of such an app and probably wouldn't sign up until they reap some of its benefits. We resolved to use active verbs on the welcome page and skip right to the good stuff. The user can sign up once they want to start customizing their experience.
                                        <img src="images/project-images/WelcomePage.png" alt="" class="responsive" width="570"/></li>
                                    <li><b>Helper text: </b>This one is pretty straightforward. Our goal was to create a new experience for finding travel plans. We wanted to be different from the existing apps out there. So it's not a stretch to believe that users could benefit from some onboarding and tutorial experience. Even though it conflicts with our effort to remove barriers to entry, we included features like Bucket List and Sell-back whose uses aren't immediately intuitive. By requiring a brief, friendly onboarding experience, we welcome our users to test out the keys that set us apart from other apps. <img src="images/project-images/Onboarding.png" alt="" class="responsive" width="570"/></li>
                                    </ul>
								</p>
