								<p class="lead">
								Imagine, if you will, that your Aunt Sophie, whose eyesight is now mostly diminished, goes out shopping alone. She is independent and gets to the store without a problem, but in order to buy items that match her kitchen decor, she'll need  help from someone in the store.
							</p>
								<p>
									This is not a problem I would have expected to be so common had I not visited some blind and visually-impaired people and asked them about some of their most common daily difficulties. Picking out home decor at IKEA stood out as a problem we could solve within our six-week time span. My team, Adappt, and I originally sought to design a wayfinding tool for blind and visually-impaired users to navigate unfamiliar buildings. It didn't take long for us to realize that the market is flooded with wayfinding apps and, honestly, wayfinding is one of the first things blind people become good at. Some tasks, like matching fabrics, shopping at Costco, or asking a stranger for help and hoping they will be friendly remain difficult despite the myriad of wayfinding apps. 
									</p>
									<p>
									To kick off our design process, we conducted some ethnographic research, developed some personas, and then brainstormed roughly 50 ideas, ranging from geofence technology to audiomaps. Sketching some of my favorite ideas helped me determine which ones would be most feasible.
