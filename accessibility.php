								<h3>Getting the ball rolling</h3>
								<p>
									From the moment we decided on the Assist idea, we had four weeks to design, test, iterate, and present a final prototype. Initially, I struggled to figure out where to begin. Drawing on what's now one of my personal design principles--"Shut up; just draw!"--I forced myself to begin sketching out some paper prototypes. By getting the initial ideas on paper, I began to visualize the application and caught myself on a number of flaws in my thinking right off the bat, such as side menu placement and bottom navigation items. My finalized lo-fi prototype is shown below as an animated gif. 
