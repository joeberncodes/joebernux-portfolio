								<p>
									<ul>
                                    	<li>Establish trust by proactively warning users of courtesy and etiquette upon signing up</li>
                                        <li>Establish more trust by allowing users to filter by ratings and restrict users with low ratings</li> 
                                        <li>Loading screens matter--even on a paper prototype</li>
                                        <li>Radio buttons are very ineffective for screen readers</li>
                                        <li>Timers and dynamic actions are difficult to test on paper and often need additional tweaking after the first hi-fi prototype</li>
