								<h3>Making it accessible</h3>
								<p>
									Assist consists of two separate interfaces--one for blind users and one for sighted volunteers. The idea is that sighted users log in and go on standby. Meanwhile, if a blind user needs assistance, she enters the details of her request, how long she expects it to take, and sends out a ping to nearby volunteers, who may accept or decline the request. Designing the sighted user interface was pretty straightforward, but in order to properly test the paper prototype for blind users, we had to properly simulate accessibility tools like screenreaders. My teammate Brittany, who regularly works with screenreaders, helped us develop a script that we read as our test users gestured. Designing for screenreaders posed many design conundrums: the most visually sensible layout often contradicts the quickest flow when examining items from top to bottom and left to right. 
								</p>
