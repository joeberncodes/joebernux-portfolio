								<p class="mb-medium">
									Coming from an engineering background, I had little prior experience with visual design elements: color theory, balance, gestalt principles. This was an exciting project I undertook to showcase what I learned in my Visual Communication class. After undergoing several rounds of design critiques and defense, I practiced the true nature of iterative design; not only is the final design "pretty," but it is internally and logically consistent. 
 
I am a big fan of music, so I chose the lecture topic of user experience as it relates to the performing arts. I found four speakers who could speak to the design of lighting, music, stage production, and acoustics. These come together to form Lights, Camera, Design, explained in detail below.
								</p>

