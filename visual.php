								<h3>Mobile first</h3>
								<p>
									I began this project by building a mobile wireframe using Adobe Illustrator. I knew that beginning in a narrowly confined visual space would allow me to easily expand into a desktop site. Doing the opposite can be more difficult. I chose to use bright gold as a visual cue for any interactive elements on the page.
								</p>
