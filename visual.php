								<h3>Mind the fold</h3>
								<p>
									Expanding the mobile site was easy, but I took the opportunity to add some wide graphics and build myself an eight-piece grid using 20px gutter width. This provided me with clean lines along which I could align elements. I carefully placed elements above the fold to look like a typical home page and added the schedule and bios of speakers below. Though the user could navigate to the schedule and directions using the navbar, users are more likely to see information if they can scroll rather than waiting for a new page to load. 
								</p>
