								<h3>The future of interactions</h3>
									<p>
									As a final project for this class, we assembled groups of four to envision an Internet of Things (IoT) technology that would be feasible in the next decade. Rather than prototype this interaction in Axure, we were assigned to create a video sketch that would demonstrate these interactions. My team set out to design an idea with the ultimate goal of removing the cell phones from young people's hands at restaurants; we set out to improve the face-to-face interactions in dining and casual bar situations.  
									</p>
