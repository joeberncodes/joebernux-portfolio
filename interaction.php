							<div class="col-md-8 col-md-offset-2 mb-medium">
								<h3>Improving the interactions</h3>
									After a round of design critique, my peers and professors pointed out a number of confusing interactions in my first draft. For one thing, while I appropriately used a Floating Action Button (FAB) as per Material Design guidelines, I unwisely chose the "record video" action for that button. Classmates were also unhappy with the use of toggle switches and the vague distinctions between subscribing to a streamer and liking a video. I came back to class to hand in my final version with these following changes: <br>
                                    <ul>
                                    	<li>I changed the FAB to be a search button, which is much more common for a user to use for this app given the “lurker to content-creator ratio.” I also removed the FAB entirely from pages where it is not relevant.</li>
                                        <li>In changing the FAB, I moved the record button to the middle of the bottom nav, where the search icon used to be.</li>
                                        <li>I deprecated the “connect additional hardware” button to indicate its lack of importance and added a microphone icon to hint at what kind of additional hardware may be added. I enlarged the “begin casting” button and changed its color to the material contrast color as a call to action.</li>
                                        <li>I changed toggle switches to tab navigation and avoided toggling whenever possible to fit content into the same view.</li>
                                        <li>I added a text input to the chat window so that users can contribute to the discussion. This was especially important to add for the user actively casting, since the conversational feedback is an important interaction.</li>
                                        <li>I re-grouped items in the video view so that the eye and heart icon are the same size, indicating that they both apply only to this specific video. I moved the subscribe button to be directly next to the user’s name, indicating that subscribing applies to the user, not to the video.</li>
                                        <li>I removed the “account” page from the side nav (hamburger menu) and instead added an “edit account” link to the profile page. This reduces the confusion of distinguishing between the two (profile is the user’s public persona on the site; account involves their password, email settings, etc.).</li>
                                        <li>I changed the colors of the recording icons from the material contrast color yellow to the more conventional red to indicate a recording status.</li>
                                        </ul>
                                   
                                   </p>
