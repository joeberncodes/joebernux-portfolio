                                	My role in this project was to photograph the scenarios, ideate and plan out the group's course of action, and to develop the Battleship and "send-a-drink" UI using Adobe Illustrator. With the excellent video editing skills of one teammate, we received some of the best feedback from our class. Please view the video below.
